    <h3>但し書き</h3>
    <ul>
    <li>あくまで全ての軽量化Modのリストを作成したいという考えに基づいて作成しているため、このリストに載っている全てのModを導入することは推奨しません</li>
    <li>便利系やバグ修正など軽量化を目的としていないModはあえて掲載していません</li>
    </ul>

